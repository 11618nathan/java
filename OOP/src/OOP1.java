//	--  속성 :  정량적인 수치로 표현가능한 객체의 구성요소들... 
//	--  행동 :  객체가 수행할수 있는 기능  

//    - 학생   (사람 )
//	   속성 :  학번, 나이, 이름, 성적, 학과, 지도교수... 
