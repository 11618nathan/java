//		행동 : 전화통화 ,  문자메시지,  인터넷, 알람, 메모... 

//    - 봄     (계절 ) 
//		  속성 : 기간 , 기온,  습도, 꽃들, 일교차  
//		  행동 : 황사 , 편서풍 .. 

//	  - 산불   ( 재해)  
//		   속성 : 피해면적, 발생지, 발생시간, 규모 
