
public class conditional 
{
	public static void main(String[] args) 
	{
		int num = 11618;
		 
		 // if - 조건식에 실행 여부 결정
		 if(num>=11618)
		 {
			 System.out.println("if문 실행");
		 }
		 
		 // if - else if - else
		 if(num > 11618)
		 {
			 System.out.println("if문 실행");
		 }
		 else if(num == 11618)
		 {
			 System.out.println("if else문 실행");
		 }
		 else
		 {
			 System.out.println("else문 실행");
		 }
	}
}
